export type Messages = typeof messages.fr;

export function getMessages(locale: Locale): any {
  return messages[locale];
}

// Function to load messages from JSON files (for dynamic loading)
export async function loadMessagesFromJSON(locale: Locale): Promise<any> {
  try {
    const response = await fetch(`/messages/${locale}.json`);
    if (!response.ok) {
      throw new Error(`Failed to load messages for locale: ${locale}`);
    }
    return await response.json();
  } catch (error) {
    console.warn(`Failed to load JSON messages for ${locale}, falling back to embedded messages:`, error);
    return messages[locale];
  }
}